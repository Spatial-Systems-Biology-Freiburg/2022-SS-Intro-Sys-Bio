#!/usr/bin/env python3

if __name__ == "__main__":
    # Eine String-Variable
    w = "Das hier ist ein ganzer Satz."

    # Erster Buchstabe
    print(w[0])

    # Letzter Buchstabe
    print(w[-1])

    # Teile an bestimmtem Zeichen auf
    print(w.split(" "))

    # Tausche bestimmte Zeichen
    print(w.replace("e", "aiai"))
